class Operators2 {
    public static void main(String[] args) {
        // Assignment
        // Arithematic
        // Augmented Assignment

        // Unary Operators (+,-,++,--,!)
        int a = 1;
        System.out.println(a);

        //increment Operator
        a = a+1;
        ++a;
        System.out.println(++a);
        System.out.println(a);

        boolean flag = !true;
        System.out.println(flag);

        byte num = -4;
        System.out.println(num>>>1);

        System.out.println(Integer.MAX_VALUE);

    }

}
